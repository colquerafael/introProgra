
public class TiposDatos{
    public static void main(String []args){
        
        //TIPOS DE DATOS PRIMITIVOS.
        byte /*tipo de la variable*/ enteros /*es el nombre de esa variable*/;
        enteros = 8; //darle un valor a la variable.
        System.out.println("Numero entero: "+ enteros);
        short mas = 16;
        int entero1 = 32;
        long largo = 64;
        
        //NUMEROS CON DECIMAL O REALES.
        float decimal1 = 32.3f;
        double decimal2 = 64.5; //no hay necesidad de poner una letra despues solo en "FLOAT"
        
        //TIPO CARACTER.
        char caracter = 'a';
        
        //TIPO BOOLEANO.
        boolean decision = true; //solo puede ser (true) o (false)
        
    }
}